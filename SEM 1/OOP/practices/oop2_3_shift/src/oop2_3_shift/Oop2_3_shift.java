package oop2_3_shift;

/**
 *
 * @author Asus
 */
public class Oop2_3_shift {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x = 7;
        System.out.println("x= " + x);
        System.out.println("x>>2= " + (x>>2));
        System.out.println("x<<1= " + (x<<1));
        System.out.println("x>>>1= " + (x>>>1));
    }

}

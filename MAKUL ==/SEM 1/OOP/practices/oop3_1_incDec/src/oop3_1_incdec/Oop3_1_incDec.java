package oop3_1_incdec;

/**
 *
 * @author Asus
 */
public class Oop3_1_incDec {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int x=8,y=13;
        System.out.println("x = "+x);
        System.out.println("y = "+y);
        System.out.println("x = "+ ++x);
        System.out.println("y = "+ y++);
        System.out.println("x = "+ x--);
        System.out.println("y = "+ --y);   
    }
}
